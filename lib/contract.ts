// コントラクトアドレス（デプロイ後に設定）
export const getContractAddress = (): `0x${string}` | undefined => {
  const address = process.env.NEXT_PUBLIC_NFT_CONTRACT_ADDRESS
  if (!address) {
    console.warn('NFT contract address not configured. Please deploy the contract and set NEXT_PUBLIC_NFT_CONTRACT_ADDRESS')
    return undefined
  }
  return address as `0x${string}`
}

// コントラクト設定
export const getContractConfig = () => {
  const address = getContractAddress()
  if (!address) {
    return null
  }

  return {
    address,
    abi: CHEERAIN_NFT_ABI,
  }
}
